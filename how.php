			<p><font size="4">
				Here are our Top 5 Tips to enjoy your soap to the very last bubble: <br><br>

1. Store soaps not in use in a covered container that allows air circulation, and in a cool, dry location. Shoe boxes work great for this. If you prefer to store your soaps in plastic, be sure to add ventilation to the container to allow the soaps to breathe. Handmade natural soaps are high in natural glycerin and vegetable oils; non-ventilated storage may result in a “weepy” or rancid soap bar. Keep your stored soaps out of direct sunlight, as natural ingredient colors may fade. Storage life varies, depending upon the formulation of the soap. In general, assuming proper storage, your handcrafted soap should last from 6 to 12 months. <br><br>

2. If storing several soaps, it’s best to store similar scents together. Sample categories include: lavender, citrus, mint, spice, tea tree, rosemary, eucalyptus, fruit and floral. The essential oils in handcrafted soaps vary in dominance and mellow over time at different rates. Storing a peppermint (dominant) scent with a citrus (mild) scent may result in the citrus scent taking on a peppermint aroma. Even under the best conditions, citrus scents tend to mellow more quickly than other scents. <br><br>

3. Cut the larger bars in half and use half at a time, storing the unused portion as noted above. Create a “buffet” of half-bar soaps and enjoy a variety of scents during the week! <br><br>

4. Use a soap dish that offers drainage (e.g. slotted) so the soap can dry between uses. Slotted soap dishes come in many different sizes, shapes and materials. A Google search will offer options such as wooden, ceramic and plastic. Another option is a flexible plastic suction cup soap saver – they’re inexpensive and work well both in the shower and at the sink. Wooden soap dishes can be used in the shower, if there is a high shelf available out of direct contact with water – and if there is adequate ventilation to allow drying between uses. We do not recommend using a ceramic soap dish in the shower, as it may fall and break. <br><br>

5. Use a soap saver pouch. Place your bar of soap in the pouch and pull the drawstring closed. Wet the soap and pouch and lather up. When done hang up the pouch, soap and all, to allow it and your soap to dry between uses. Soap saver pouches come in several different materials, such as cotton yarn, ramie and plastic mesh. In addition to lengthening the life of your soap, a soap saver pouch will add a gentle exfoliating scrub. Soap savers are great for using up soap slivers – gather slivers together in the pouch and use until gone. <br><br>

With proper storage and in-use care, you’ll get the most of your handmade natural soap! </font>

